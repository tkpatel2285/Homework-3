public class NumberPattern
{

    public static void main(String[]args)
    {       int i,j,n=0;
        for ( i =1;i <=5;i++)
        {
            for ( j =1;j<=i;j++)
            {
                    n++;
                System.out.print(" " + n);



            }

            System.out.println();
        }


    }
}
